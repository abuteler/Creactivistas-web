	<h2>PROPUESTA</h2>
	<p>Muchas organizaciones todavía  enfrentan los desafíos de la gestión del talento de manera asilada dejando la atracción en manos de las consultoras de selección o agencias de comunicación; el <em>desarrollo</em> al área de Recursos Humanos ; y la <em>retención</em> librada a cuestiones de mercado.</p>
	<p>Poder pensar y diseñar un Programa de Gestión del Talento integral le permite a una compañía hacerse dueña del proceso más vital para el éxito y sustentabilidad de su proyecto.</p>
	<p>Un Programa de Gestión de Talento se construye a partir de la definición de talento de cada organización y se compone de instancias individuales, de equipo e institucionales que combinan comunicación, coaching y neurociencias para atraer, desarrollar y retener personas con talento.</p>
